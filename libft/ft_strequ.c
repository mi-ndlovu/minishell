int		ft_strequ(char const *s1, char const *s2)
{
	unsigned int i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
			i++;
		if ((s1[i] - s2[i]) == 0)
			return (1);
		else if ((s1[i] - s2[i]) != 0)
			return (0);
	}
	return (0);
}
