#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char c2;
	char *s2;

	s2 = (char*)s;
	c2 = (char)c;
	while (*s2 != c2)
	{
		if (!*s2)
		{
			return (NULL);
		}
		s2++;
	}
	return (s2);
}
